"""Tests for semantic leaderboard handles — `<owner>/<slug>`.

Covers the auto-generated `user.username` + `leaderboard.slug`, the
`/api/leaderboard/resolve` endpoint, the `<owner>/<slug>` web route, and the
`client.leaderboard(ref)` handle (plus flat methods accepting a handle).
"""
from __future__ import annotations

import json

import numpy as np

import benchhub as bh
from app import (
    Dataset,
    Leaderboard,
    Sample,
    User,
    db,
    generate_api_token,
    _lb_client_ref,
    _resolve_lb_ref,
)


def _mk_user(email, name, sub):
    u = User(email=email, display_name=name, oauth_provider='github',
             oauth_sub=sub, api_token=generate_api_token())
    db.session.add(u)
    db.session.commit()
    return u


def _mk_lb(name, owner_user_id=None, pred=None):
    lb = Leaderboard(
        name=name, summary_metrics='', visibility='public',
        owner_user_id=owner_user_id,
        required_pred_fields_json=(json.dumps(pred) if pred else None),
    )
    db.session.add(lb)
    db.session.commit()
    return lb


# --- auto-generated handles -------------------------------------------------

def test_username_autogenerated_and_unique(db_session):
    a = _mk_user('a@x.io', 'Ada Lovelace', 'u-a')
    b = _mk_user('b@x.io', 'Ada Lovelace', 'u-b')  # same display name
    assert a.username == 'ada-lovelace'
    assert b.username == 'ada-lovelace-2'           # collision suffix


def test_username_falls_back_to_email_for_non_ascii_name(db_session):
    # A display name with no usable ASCII chars uses the email local-part.
    u = _mk_user('mailortal@gmail.com', 'אורטל', 'u-heb')
    assert u.username == 'mailortal'


def test_lb_slug_autogenerated_unique_per_owner(db_session):
    u = _mk_user('o@x.io', 'Owner One', 'u-o')
    lb1 = _mk_lb('ADE20K Scene Parse 150', owner_user_id=u.id)
    lb2 = _mk_lb('ADE20K Scene Parse 150', owner_user_id=u.id)
    assert lb1.slug == 'ade20k-scene-parse-150'
    assert lb2.slug == 'ade20k-scene-parse-150-2'
    assert _lb_client_ref(lb1) == 'owner-one/ade20k-scene-parse-150'


def test_owner_less_lb_uses_community_handle(db_session):
    lb = _mk_lb('Community Board')  # no owner
    assert lb.slug == 'community-board'
    assert _lb_client_ref(lb) == 'community/community-board'


def test_resolve_helper_forms(db_session):
    u = _mk_user('r@x.io', 'Res Olver', 'u-r')
    lb = _mk_lb('My Board', owner_user_id=u.id)
    assert _resolve_lb_ref(str(lb.id)).id == lb.id          # by id
    assert _resolve_lb_ref(f'{u.username}/{lb.slug}').id == lb.id  # owner/slug
    assert _resolve_lb_ref(lb.slug).id == lb.id             # bare unambiguous slug
    assert _resolve_lb_ref('nobody/nope') is None
    assert _resolve_lb_ref('') is None


# --- HTTP surface -----------------------------------------------------------

def test_resolve_endpoint(client, db_session):
    u = _mk_user('r2@x.io', 'Api Caller', 'u-r2')
    lb = _mk_lb('Resolve Board', owner_user_id=u.id)
    ref = f'{u.username}/{lb.slug}'

    r = client.get(f'/api/leaderboard/resolve?ref={ref}')
    assert r.status_code == 200
    body = r.get_json()
    assert body['id'] == lb.id
    assert body['ref'] == ref
    assert body['owner'] == u.username
    assert body['slug'] == lb.slug

    # an integer id resolves too
    assert client.get(f'/api/leaderboard/resolve?ref={lb.id}').get_json()['id'] == lb.id
    # unknown handle -> 404
    assert client.get('/api/leaderboard/resolve?ref=nobody/nope').status_code == 404


def test_resolve_gates_private_boards(client, db_session):
    """A private board 404s to anonymous callers but resolves for its owner
    via a Bearer token (mirrors the contract endpoint's visibility policy)."""
    u = _mk_user('p@x.io', 'Priv Owner', 'u-p')
    lb = Leaderboard(name='Secret Board', summary_metrics='',
                     owner_user_id=u.id, visibility='private')
    db.session.add(lb)
    db.session.commit()
    ref = f'{u.username}/{lb.slug}'
    # anonymous -> 404 (don't leak existence)
    assert client.get(f'/api/leaderboard/resolve?ref={ref}').status_code == 404
    # owner with Bearer token -> 200
    r = client.get(f'/api/leaderboard/resolve?ref={ref}',
                   headers={'Authorization': f'Bearer {u.api_token}'})
    assert r.status_code == 200
    assert r.get_json()['id'] == lb.id


def test_web_route_redirects_to_canonical_id(client, db_session):
    u = _mk_user('w@x.io', 'Web User', 'u-w')
    lb = _mk_lb('Redirect Board', owner_user_id=u.id)
    r = client.get(f'/leaderboard/{u.username}/{lb.slug}')
    assert r.status_code in (301, 302)
    assert f'/leaderboard/{lb.id}' in r.headers['Location']
    # bogus handle -> 404
    assert client.get('/leaderboard/nobody/nope').status_code == 404


# --- client handle object ---------------------------------------------------

def test_client_handle_end_to_end(client, db_session):
    u = _mk_user('h@x.io', 'Handle User', 'u-h')
    ds = Dataset(name='handle_ds', visibility='public')
    db.session.add(ds)
    db.session.flush()
    db.session.add(Sample(dataset_id=ds.id, name='s0'))
    lb = _mk_lb('Handle Board', owner_user_id=u.id, pred=[
        {"name": "depth_pred", "kind": "depth",
         "params": {"unit": "meters"}, "role": "pred"}])
    lb.datasets.append(ds)
    db.session.commit()
    ref = _lb_client_ref(lb)

    bh_client = bh.Client(token=u.api_token, base_url='http://test',
                          transport=bh.FlaskTestClientTransport(client))
    handle = bh_client.leaderboard(ref)
    assert isinstance(handle, bh.LeaderboardHandle)
    assert handle.id == lb.id
    assert handle.ref == ref

    contract = handle.contract()
    assert any(c['name'] == 'depth_pred' for c in contract)

    sub = handle.submission(name='via-handle')
    sub.predict('s0', depth_pred=bh.Depth(np.ones((4, 4), np.float32), unit='meters'))
    result = sub.submit()
    assert result['leaderboard_id'] == lb.id
    assert result['samples'] == 1


def test_update_username_changes_handle(auth_client, logged_in_user, db_session):
    lb = _mk_lb('My Eval', owner_user_id=logged_in_user.id)
    r = auth_client.post('/settings/account/username',
                         data={'username': 'Cool Handle'})  # slugified server-side
    assert r.status_code in (302, 303)
    db.session.refresh(logged_in_user)
    db.session.refresh(lb)
    assert logged_in_user.username == 'cool-handle'
    assert _lb_client_ref(lb) == 'cool-handle/my-eval'


def test_update_username_rejects_taken_and_reserved(auth_client, logged_in_user, db_session):
    other = _mk_user('other@x.io', 'Other Person', 'u-other')
    other.username = 'taken-name'
    db.session.commit()
    before = logged_in_user.username

    auth_client.post('/settings/account/username', data={'username': 'taken-name'})
    db.session.refresh(logged_in_user)
    assert logged_in_user.username == before  # unchanged — name was taken

    auth_client.post('/settings/account/username', data={'username': 'admin'})
    db.session.refresh(logged_in_user)
    assert logged_in_user.username == before  # unchanged — reserved


